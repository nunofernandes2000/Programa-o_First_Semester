package T1_VAR;

public class Exemplo1Divisão {
    public static void main(String[] args) {
        int i = 31;
        float f = 31f;

        int divisao_int = i / 5;
        float divisao_float = f / 5;
        System.out.println("Divisao_int:  " +divisao_int);
        System.out.println("Divisao_float:  "+divisao_float);
        System.out.println();
        System.out.println("30/5=  "+30/5);
        System.out.println("Divisão 31/5=  "+31/5);
        System.out.println("Resto 31/5=  " +31%5);


    }
}


